    CLIENT_NAME VARCHAR2(50) NOT NULL,                                   -- 회원명
    REGION_ID VARCHAR2(50) NOT NULL,                                      -- 지역ID
    RELIABLITY NUMBER(3) DEFAULT 0 CHECK(RELIABLITY BETWEEN 0 AND 100) -- 신뢰도 
)
